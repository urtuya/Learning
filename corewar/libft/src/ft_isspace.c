#include "libft.h"

int	ft_isspace(int c)
{
	if (c != 9 && (c < 11 || c > 13) && c != 32)
		return (0);
	//return ((c >= 9 && c <= 13) || c == 32 ?
	//		1 : 0);
	return (1);
}
